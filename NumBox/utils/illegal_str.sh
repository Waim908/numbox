if [[ -n "$1" ]] &&
   [[ "$1" != "." && "$1" != ".." ]] &&
   [[ "$1" != */* ]] &&
   [[ "$1" != -* ]] &&
   [[ ! "$1" =~ [[:cntrl:]] ]] &&
   [[ "$1" =~ ^[^[:space:]].*[^[:space:]]$ ]] &&
   [[ "$1" != *~* ]] &&          # 新增：检查不包含 ~
   [[ "$1" != *\$* ]]           # 新增：检查不包含 $
then
    str_is=good
else
    str_is=bad
fi